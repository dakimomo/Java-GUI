    private void openfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openfileActionPerformed
       //Opens up an "Open File" file chooser dialog.
        int returnVal = jfc.showOpenDialog(openfile); 
        String line;
         //Using a string builder inorder to display text folders when opened.
        StringBuilder sb = new StringBuilder();
         
         try {
             if (returnVal == JFileChooser.APPROVE_OPTION){
              textPane.setText("");     //Set to null string to prepare it for file text.
              f = jfc.getSelectedFile();   //Returns the selected file.
              fr = new FileReader(f);
              buffer = new BufferedReader(fr);
              
              while((line =buffer.readLine()) !=null){
                   sb.append(line);
                
              }
               
              textPane.setText(sb.toString());
              fr.close();
              buffer.close();
              textPane.setCaretPosition(0);
              
             } else if (returnVal == JFileChooser.CANCEL_OPTION){
                  //NO action
             } 
             } catch (IOException e){
                     showMessageDialog(jMenuBar1,e.getMessage(),
                             "Error!", ERROR_MESSAGE);
              
              }
                       
        
    }//GEN-LAST:event_openfileActionPerformed

