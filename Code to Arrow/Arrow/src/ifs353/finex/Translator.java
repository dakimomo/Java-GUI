    private void arrowsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_arrowsActionPerformed
     String integers;
     String [] integerarray;
     int [] codes;
        
      integers = textPane.getText();  //Gets users string input from the Textpane
      integerarray = integers.split(" "); //Splits the string into various sub-strings 
      codes = new int [integerarray.length]; //Stores the String of integers into an array.
         
 
     try{
             
       for(int i = 0; i <integerarray.length; i++){ // Loops through the array
           codes [i] = Integer.parseInt(integerarray[i]); //Parses the string input as a integer
         }
       

      }catch (NumberFormatException e){ //catches a number format exception
              showMessageDialog(null,"NumberFormatException\n"+ e.getMessage(),"ERROR",
                  ERROR_MESSAGE);
      }

      
       arrowResult = translate.toArrowsString(codes);//Stores the arrow symbols into a String called ArrowResult
       textPane.setText(arrowResult); //Dispalys Arrow symbols onto the TextPane
    }//GEN-LAST:event_arrowsActionPerformed

    private void codesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codesActionPerformed
        char[] arrows;
        arrowResult = arrowResult.replaceAll("\\s", ""); //Finds single whitespace characters and replaces them with an empty string.
        
        arrows = arrowResult.toCharArray(); //Converts the string of Arrow symbols to their integer code sequence. 
        
        textPane.setText(translate.toIntegersString(arrows)); //Dispalys the integers onto the TextPane
    }//GEN-LAST:event_codesActionPerformed

